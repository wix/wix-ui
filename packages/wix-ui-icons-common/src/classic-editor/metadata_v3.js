const toIconMetaData = (csvEntry) => {
  const returnThis = [];
  if (csvEntry['Not Used'] === 'Not Used'){
    return returnThis
  }
  if(csvEntry['New Name']){
    returnThis.push(JSON.stringify({
      title: csvEntry['New Name'],
      category: csvEntry['Category'],
      description: '',
      tags: [],
      sizes: {
        '24': csvEntry['New Name'],
        ...((csvEntry['New Name Small']) && {
          '18': csvEntry['New Name Small'],
        })
      }
    }))
  }
  if(csvEntry['New Name Thick/Filled']){
    returnThis.push(JSON.stringify({
      title: csvEntry['New Name Thick/Filled'],
      category: csvEntry['Category'],
      description: '',
      tags: [],
      sizes: {
        '24': csvEntry['New Name Thick/Filled'],
        ...((csvEntry['__1']) && {
          '18': csvEntry['__1'],
        })
      }
    }))
  }
  return returnThis
}

const toGeneralIcon = (csvEntry) => {
  if (generalIconsNameList.includes(csvEntry['New Name']) || generalIconsNameList.includes(csvEntry['New Name Thick/Filled']))
    return toIconMetaData(csvEntry)
  return []
}

const toSettingsIcon = (csvEntry) => {
  if (settingsIconsNameList.includes(csvEntry['New Name']) || settingsIconsNameList.includes(csvEntry['New Name Thick/Filled']))
    return toIconMetaData(csvEntry)
  return []
}

const toNoHomeIcon = (csvEntry) => {
  if (!settingsIconsNameList.includes(csvEntry['New Name']) && !settingsIconsNameList.includes(csvEntry['New Name Thick/Filled'])
  && !generalIconsNameList.includes(csvEntry['New Name']) && !generalIconsNameList.includes(csvEntry['New Name Thick/Filled']))
    return toIconMetaData(csvEntry)
  return []
}

